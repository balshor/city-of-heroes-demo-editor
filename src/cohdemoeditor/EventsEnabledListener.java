package cohdemoeditor;

/**
 * This interface describes a class that can have its event notifications
 * enabled or disabled. When disabled, no events should be fired. Events can
 * either be cached for firing or discarded and a general update fired when
 * events are re-enabled.
 * 
 * @author Darren Lee
 */
public interface EventsEnabledListener {
	public void eventsEnabledChanged(boolean areEventsEnabled);
}
